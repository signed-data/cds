@mcp.tool()
async def get_top_headlines_br(
    category: str = "geral",
    max_results: int = 20,
) -> dict[str, Any]:
    """
    Get top Brazilian news headlines by category.
    Headlines are signed by signed-data.org — source, URL, and timestamp are attested.
    No article body is returned (copyright).

    Args:
        category: One of "geral", "economia", "politica", "esportes",
                  "tecnologia", "saude", "entretenimento", "mercado".
                  Defaults to "geral".
        max_results: Number of headlines to return (1–50, default 20).
    """
    cat = category.lower().strip()
    query = CATEGORY_QUERIES.get(cat, f"Brasil {category}")
    max_results = max(1, min(max_results, 50))

    headlines = await _gdelt_search(query, timespan="24h", max_records=max_results)
    if not headlines:
        headlines = await _gdelt_search(query, timespan="7d", max_records=max_results)

    summary = (
        f"Top {len(headlines)} headline(s) brasileiros — categoria: {category} "
        f"— fonte: GDELT"
    )

    event = CDSEvent(
        content_type=CDSVocab.NEWS_HEADLINE,
        source=SourceMeta(id=CDSSources.GDELT),
        occurred_at=datetime.now(UTC),
        lang="pt-BR",
        payload={
            "category": category,
            "headlines": headlines,
            "total": len(headlines),
            "query_timestamp": datetime.now(UTC).isoformat(),
        },
        event_context=ContextMeta(summary=summary, model="rule-based-v1"),
    )
    signer = _get_signer()
    if signer:
        signer.sign(event)
    return _event_to_dict(event)


