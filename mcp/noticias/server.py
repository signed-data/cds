# ── Runtime config ──────────────────────────────────────────
_PRIVATE_KEY_PATH = os.environ.get("CDS_PRIVATE_KEY_PATH", "")
_ISSUER           = os.environ.get("CDS_ISSUER", "signed-data.org")


def _get_signer() -> CDSSigner | None:
    if _PRIVATE_KEY_PATH and Path(_PRIVATE_KEY_PATH).exists():
        return CDSSigner(_PRIVATE_KEY_PATH, issuer=_ISSUER)
    return None


def _event_to_dict(event: CDSEvent) -> dict[str, Any]:
    return {
        "cds_event_id": event.id,
        "content_type": event.content_type,
        "occurred_at": event.occurred_at.isoformat(),
        "signed_by": event.integrity.signed_by if event.integrity else None,
        "hash": event.integrity.hash[:20] + "..." if event.integrity else None,
        "summary": event.event_context.summary if event.event_context else "",
        "payload": event.payload,
    }


def _parse_gdelt_date(seendate: str) -> str:
    """Convert GDELT seendate (20260427T143000Z) to ISO 8601."""
    try:
        dt = datetime.strptime(seendate, "%Y%m%dT%H%M%SZ").replace(tzinfo=UTC)
        return dt.isoformat()
    except ValueError:
        return seendate


def _gdelt_article_to_headline(art: dict[str, Any]) -> dict[str, Any]:
    return {
        "headline": art.get("title", ""),
        "source_name": art.get("domain", ""),
        "source_url": art.get("url", ""),
        "published_at": _parse_gdelt_date(art.get("seendate", "")),
        "language": art.get("language", ""),
        "image_url": art.get("socialimage") or None,
    }


async def _gdelt_search(
    query: str,
    timespan: str = "7d",
    max_records: int = 25,
    source_lang: str = "Portuguese",
) -> list[dict[str, Any]]:
    params: dict[str, str] = {
        "query": query,
        "mode": "artlist",
        "format": "json",
        "maxrecords": str(min(max_records, 50)),
        "timespan": timespan,
    }
    if source_lang:
        params["sourcelang"] = source_lang
    async with httpx.AsyncClient(timeout=HTTP_TIMEOUT) as client:
        resp = await client.get(GDELT_BASE, params=params)
        resp.raise_for_status()
        data = resp.json()
    articles: list[dict[str, Any]] = data.get("articles") or []
    return [_gdelt_article_to_headline(a) for a in articles]


